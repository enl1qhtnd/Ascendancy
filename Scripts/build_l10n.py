def build_locale_map() -> dict[str, dict[str, str]]:
    locale_map: dict[str, dict[str, str]] = {}
    missing_by_locale: dict[str, list[str]] = {}
    for loc in LOCALES:
        values = merge_locale_dicts(
            IDENTITY_TRANSLATIONS,
            BASE_LOCALE_MAP[loc],
            LOCALE_SUPPLEMENTS.get(loc, {}),
        )
        missing = [key for key in KEYS if key not in values]
        if missing:
            missing_by_locale[loc] = missing
        locale_map[loc] = {key: values[key] for key in KEYS if key in values}

    if missing_by_locale:
        details = "; ".join(
            f"{loc}: {', '.join(keys)}" for loc, keys in missing_by_locale.items()
        )
        raise RuntimeError(f"Missing localization entries: {details}")
    return locale_map


LOCALE_MAP = build_locale_map()


def build_localizable(existing: dict | None = None) -> dict:
    """Assemble the ``strings`` map for Localizable.xcstrings.

    Script-managed keys (the ones in ``KEYS``) get fresh translations from
    ``LOCALE_MAP``. Keys that already exist in the catalog but aren't in
    ``KEYS`` — typically auto-extracted entries Xcode adds during a GUI build
    (e.g. ``""``, ``"–"``, ``"· %@"``) — are preserved verbatim so a GUI
    build doesn't keep re-adding them and producing diff churn.
    """
    strings: dict = {}
    seen_symbols: set[str] = set()
    for key in KEYS:
        symbol_key = symbol_collision_key(key)
        entry = {"extractionState": "manual"}
        if symbol_key in seen_symbols:
            entry["generatesSymbol"] = False
        else:
            seen_symbols.add(symbol_key)

        locs = {}
        for loc in LOCALES:
            val = LOCALE_MAP[loc][key]
            locs[loc] = {"stringUnit": {"state": "translated", "value": val}}
        entry["localizations"] = locs
        strings[key] = entry

    if existing:
        managed = set(KEYS)
        for key, value in existing.get("strings", {}).items():
            if key not in managed:
                strings[key] = value
    return strings


