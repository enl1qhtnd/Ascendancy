def merge_locale_dicts(*dicts: dict[str, str]) -> dict[str, str]:
    m: dict[str, str] = {}
    for d in dicts:
        m.update(d)
    return m


def symbol_collision_key(key: str) -> str:
    return re.sub(r"[^0-9a-z]+", "", key.lower())


